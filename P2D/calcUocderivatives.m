function dUocdc_ss = calcUocderivatives(c_ss, infc, coefs)

    % F = 96485.33289; % Faraday's constant (C/mol)
    % R = 8.31447; % Gas constant
    % T = 373.15; % Absolute temperature

    % maximum conc in cathode active material (mol/m^3)
    c_max = coefs.c_max;

    y = c_ss / c_max(infc);

    if (infc == 1)

        dUocdc_ss = -1.5 * (120.0 / c_max(1)) * exp(-120.0 * y) ...
            +(0.0351 / (0.083 * c_max(1))) * ((cosh((y - 0.286) / 0.083)).^(-2)) ...
            -(0.0045 / (c_max(1) * 0.119)) * ((cosh((y - 0.849) / 0.119)).^(-2)) ...
            -(0.035 / (c_max(1) * 0.05)) * ((cosh((y - 0.9233) / 0.05)).^(-2)) ...
            -(0.0147 / (c_max(1) * 0.034)) * ((cosh((y - 0.5) / 0.034)).^(-2)) ...
            -(0.102 / (c_max(1) * 0.142)) * ((cosh((y - 0.194) / 0.142)).^(-2)) ...
            -(0.022 / (c_max(1) * 0.0164)) * ((cosh((y - 0.9) / 0.0164)).^(-2)) ...
            -(0.011 / (c_max(1) * 0.0226)) * ((cosh((y - 0.124) / 0.0226)).^(-2)) ...
            +(0.0155 / (c_max(1) * 0.029)) * ((cosh((y - 0.105) / 0.029)).^(-2));

    elseif (infc == 2)

        dUocdc_ss = 0.07645 * (-54.4806 / c_max(2)) * ...
            ((1.0 ./ cosh(30.834 - 54.4806 * y)).^2) ...
            +2.1581 * (-50.294 / c_max(2)) * ((cosh(52.294 - 50.294 * y)).^(-2)) ...
            +0.14169 * (19.854 / c_max(2)) * ((cosh(11.0923 - 19.8543 * y)).^(-2)) ...
            -0.2051 * (5.4888 / c_max(2)) * ((cosh(1.4684 - 5.4888 * y)).^(-2)) ...
            -0.2531/0.1316 / c_max(2) * ((cosh((-y + 0.56478) / 0.1316)).^(-2)) ...
            -0.02167/0.006 / c_max(2) * ((cosh((y - 0.525) / 0.006)).^(-2));

    else
        error('wrong input of interface number!');
    end

end
